public class Primitives {
    @SuppressWarnings("unused")
    public static void main(String[] args) {
        System.out.println("Primitive Data types in Java");
        // Primitive Data Types
        byte smallInt = 127; // max 127 (1 Byte)
        int a = 90_000; // we can use underscore instead of comma
        int b = 23; // 4 Bytes
        long longInteger = 9999999999999L; // 8 Bytes
        short shortInteger = (short) 939932332; // 2 Bytes

        char letter = 'd'; // 2 Bytes

        float f = 9.098f; // 4 Bytes

        boolean isValid = false;

        // Wrapper Classes
        Integer inte = 89;
    }
}
